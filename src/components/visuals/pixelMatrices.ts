export function retroPetPixels(species: SpeciesId, index: number, asleep = false): string[] {
  const grid = PET_PIXELS[index].map((row) => [...row]);
  const set = (x: number, y: number, value = '1') => {
    if (grid[y]?.[x] !== undefined) grid[y][x] = value;
  };

  void species;
  const leafPixels =
    index === 0
      ? [
          [6, 1],
          [7, 0],
          [7, 1],
          [8, 1],
          [9, 0],
          [9, 1],
        ]
      : [
          [5, 2],
          [5, 1],
          [6, 2],
          [7, 1],
          [7, 0],
          [8, 1],
          [9, 2],
          [10, 1],
          [10, 2],
        ];
  leafPixels.forEach(([x, y]) => set(x, y, '4'));

  if (index > 0 && index < 8) {
    [
      [2, 8],
      [1, 9],
      [13, 8],
      [14, 9],
    ].forEach(([x, y]) => set(x, y, '2'));
    [
      [4, 13],
      [3, 14],
      [5, 14],
      [10, 13],
      [10, 14],
      [12, 14],
    ].forEach(([x, y]) => set(x, y));
  }
  if (index === 4 || index === 6) {
    [
      [5, 7],
      [6, 7],
      [9, 7],
      [10, 7],
      [6, 8],
      [9, 8],
    ].forEach(([x, y]) => set(x, y, '1'));
  }
  if (index === 5) {
    [
      [3, 10],
      [4, 11],
      [11, 11],
      [12, 10],
    ].forEach(([x, y]) => set(x, y, '3'));
  }
  if (index === 7) {
    [
      [6, 0],
      [7, 0],
      [8, 0],
      [9, 0],
      [6, 1],
      [9, 1],
    ].forEach(([x, y]) => set(x, y));
  }
  if (asleep && index > 0 && index < 8) {
    [
      [5, 6],
      [6, 6],
      [9, 6],
      [10, 6],
    ].forEach(([x, y]) => set(x, y));
    [
      [5, 7],
      [9, 7],
    ].forEach(([x, y]) => set(x, y, '2'));
  }
  return grid.map((row) => row.join(''));
}
